
import React from 'react';
import { Helmet } from 'react-helmet-async';

interface SEOProps {
  title?: string;
  description?: string;
  keywords?: string;
  image?: string;
  url?: string;
}

const SEO: React.FC<SEOProps> = ({ 
  title = "GREY | Luxury Digital Agency", 
  description = "A top 1% aesthetic website for a premium web/app agency featuring 3D motion, golden accents, and AI integration.",
  keywords = "Luxury web design, high-end digital agency, 3D websites, React developer India, Jash Mehta, GREY Agency",
  image = "./assets/images/image_01.jpg",
  url = "https://grey-agency.com"
}) => {
  const fullTitle = title.includes("GREY") ? title : `${title} | GREY`;

  return (
    <Helmet>
      {/* Standard Metadata */}
      <title>{fullTitle}</title>
      <meta name="description" content={description} />
      <meta name="keywords" content={keywords} />
      
      {/* Open Graph / Facebook */}
      <meta property="og:type" content="website" />
      <meta property="og:url" content={url} />
      <meta property="og:title" content={fullTitle} />
      <meta property="og:description" content={description} />
      <meta property="og:image" content={image} />

      {/* Twitter */}
      <meta property="twitter:card" content="summary_large_image" />
      <meta property="twitter:url" content={url} />
      <meta property="twitter:title" content={fullTitle} />
      <meta property="twitter:description" content={description} />
      <meta property="twitter:image" content={image} />
    </Helmet>
  );
};

export default SEO;
